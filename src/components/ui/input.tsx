import { cn } from "@/lib/utils";

export type InputProps = React.InputHTMLAttributes<HTMLInputElement>;

export const Input = React.forwardRef<HTMLInputElement, InputProps>(({ className, type = "text", ...props }, ref) => {
  return (
    <input
      ref={ref}
      type={type}
      className={cn(
        "flex h-11 w-full rounded-xl border border-[#6B9FB8]/40 bg-white px-4 text-base text-[#1A2B38] placeholder:text-[#8A9BAB] transition focus:border-[#6B9FB8] focus:outline-none focus-visible:ring-2 focus-visible:ring-[#6B9FB8]/60 disabled:cursor-not-allowed disabled:opacity-50",
        className,
      )}
      {...props}
    />
  );
});
Input.displayName = "Input";
